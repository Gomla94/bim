    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(StorePaymentRequest $request, Transaction $transaction)
    {
        $payment = $transaction->payments()->create([
            'amount' => $request->amount,
            'details' => $request->details,
            'payment_method' => $request->payment_method
        ]);

        $total_paid_amount = $transaction->payments()->get()->sum('amount');
        if ($transaction->amount === $total_paid_amount) {
            $transaction->update(['status' => 'paid']);
        }

        return $payment;
    }

